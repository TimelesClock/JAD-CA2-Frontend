package classes;

public class Publisher {
    private int id;
    private String name;

    public Publisher() {
    }
    
    

    public void setId(int id) {
		this.id = id;
	}



	public void setName(String name) {
		this.name = name;
	}



	public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}